                $nome = $field->nome;
                if (Str::endsWith($nome, '_id')) {
                    $relazioni[$nome] = Str::beforeLast($nome, '_id');
                } else {
                    $relazioni[$nome] = Str::snake($field->relazione_tabella);
                }
            }
        }

        $campiFinaliSearch = [];
        $campiFinaliEdit = [];
        foreach ($campi as $campo) {


            switch ($campo->tipo) {
                case 'string':
                    $campoSearchArray = [
                        'operator' => 'like',
                    ];
                    $campoEditArray = [];
                    break;
                case 'enum':
                    $campoSearchArray = [
                        'operator' => '=',
                        'options' => 'dboptions',
                    ];
                    $campoEditArray = [
                        'options' => 'dboptions',
                    ];
                    break;
                case 'boolean':
                    $campoSearchArray = [
                        'operator' => '=',
                        'options' => 'boolean',
                    ];
                    $campoEditArray = [
                        'options' => 'boolean',
                    ];
                    break;
                case 'id_integer':
                    if ($campo->relazione_tabella) {
                        $relation = Arr::get($relazioni, $campo->nome);
                    }
                    if ($relation) {
                        $campoSearchArray = [
                            'operator' => '=',
                            'options' => 'relation:' . $relation,
