                        $campoEditArray = [
                            'options' => 'relation:' . $relation,
                        ];
                    } else {
                        $campoSearchArray = [];
                        $campoEditArray = [];
                    }
                    break;

                default:
                    $campoSearchArray = [];
                    $campoEditArray = [];
                    break;
            }

            $campiFinaliSearch[$campo->nome] = $campoSearchArray;
            $campiFinaliEdit[$campo->nome] = $campoEditArray;
        }


        $relazioniFinaliList = [];
        $relazioniFinaliEdit = [];
