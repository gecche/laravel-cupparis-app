        $migrationCampi = $this->getIndent() . '$table->id();' . "\n";

        foreach ($fields as $field) {
            $migrationCampi .= $this->getIndent();


            $migrationCampi .= $this->getCampoString($field);


        }
        $stub = str_replace(
            '{{$migrationCampi}}', $migrationCampi, $stub
