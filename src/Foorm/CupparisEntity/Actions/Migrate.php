    protected function buildModelConfValues(&$fieldsArray, CupparisEntityField $entityField, $type)
    {
        if (!$entityField->$type) {
            return;
        }
        $fieldArray = [
            'nome' => $entityField->nome,
            'type' => $entityField->$type,
            'order' => in_array($entityField->tipo, [CupparisTipiCampi::STRING->value]) ? 1 : 0,
        ];

        $fieldsArray[$entityField->nome] = $fieldArray;

    }

