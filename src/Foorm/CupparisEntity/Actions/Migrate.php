    protected function saveLangFile($resourceFile, $configStub, $type = 'model')
    {
        $filename = resource_path($resourceFile);

        if (!$this->files->exists($filename)) {
            $parentDir = $this->files->dirname($filename);
            if (!$this->files->isDirectory($parentDir)) {
                $this->files->makeDirectory($parentDir, 0755, true);
            }

            $this->files->put($filename, '<?php return []; ?>');
        }


        $langs = include $filename;

        switch ($type) {
            case 'model':

                $singolare = $this->model->lang_singolare;
                $plurale = $this->model->lang_plurale;
                $langs[$this->snakeModel] = "$singolare|$plurale";

                break;
            case 'fields':
                foreach ($this->model->fields as $field) {
                    $nome = $field->nome;
                    if (!array_key_exists($nome, $langs)) {
                        $langs[$nome] = Lang::getM($nome);
                    }
                }
                break;
            default:
                break;
        }


        $modelConfigStub = str_replace(
            '{{$configArray}}', varexport($langs), $configStub
        );

        $this->files->put($filename, $modelConfigStub);

    }


