    protected function getTimestampsOwnerships($tipo, $value)
    {
        switch ($value) {
            case 'nullable':
                return $this->getIndent() . '$table->nullable' . ucfirst($tipo) . '();';
            case 1:
                return $this->getIndent() . '$table->' . $tipo . '();';
            default:
                return '';
        }
    }

    protected function getCampoString($field)
    {

        $fieldName = $field->nome;

        $fieldString = '$table->';
        $type = $field->tipo;
        $info = $field->informazioni;
        $info = array_map('trim', explode(',', $info));
        $infoString = '';

        switch ($type) {

            case 'enum':
                foreach ($info as $option) {
                    $infoString .= "'" . $option . "',";
                }
                if ($infoString) {
                    $infoString = trim($infoString, ',');
                    $infoString = ',[' . $infoString . ']';
                } else {
                    $infoString = ',[]';
                }
                break;
            default:
                $infoString = count($info) > 0 ? ',' . implode(',', $info) : '';
                break;
        }

        $infoString = rtrim($infoString, ', ');

        switch ($type) {
            case 'integer':
                $fieldString .= 'unsignedInteger(\'' . $fieldName . '\'' . $infoString . ')';
                break;
            case 'id_integer':
                $fieldString .= 'unsignedBigInteger(\'' . $fieldName . '\'' . $infoString . ')';
                break;
            case 'text':
                $fieldString .= 'longText(\'' . $fieldName . '\'' . $infoString . ')';
                break;
            default:
                $fieldString .= $type . '(\'' . $fieldName . '\'' . $infoString . ')';
                break;
        }

        if ($field->nullable) {
            $fieldString .= '->nullable()';
        }

        if ($field->default) {
            switch ($type) {
                case 'integer':
                case 'id_integer':
                case 'decimal':
                case 'boolean':
                case 'float':
                    $fieldString .= '->default(' . $field->default . ')';
                    break;

                default:
                    $fieldString .= '->default(\'' . $field->default . '\')';
                    break;
            }

        }


        $relationString = '';
        if ($field->relazione_tabella) {
            $modelClass = "App\\Models\\" . $field->relazione_tabella;
            $model = new $modelClass();

            //VINCOLO A CREARE UN INDICE NON UNICO SUL CAMPO PER LA RELAZIONE
            $relationString = '$table->foreign(\'' . $fieldName . '\')';
            $relationString .= '->references(\'' . ($field->relazione_campo ?: 'id') . '\')';
            $relationString .= '->on(\'' . $model->getTable() . '\')';
            $relationString .= '->onDelete(\'' . $field->on_delete . '\')';
            $relationString .= '->onUpdate(\'' . $field->on_update . '\')';


        }

        switch ($field->index) {
            case 'INDEX':
                $fieldString .= '->index()';
                break;
            case 'UNIQUE':
                $fieldString .= '->unique()';
                break;
            default:
                break;
        }

        $fieldString = $fieldString . ';';

        if ($relationString) {
            $fieldString = $fieldString . "\n" . $this->getIndent() . $relationString . ';';
        }
        return $fieldString;
    }


